#include <stdio.h>

int main(){
	char alphabet_lowercase, alphabet_uppercase;
	int number_one, number_two;
	float pi, fraction;

	alphabet_lowercase = 'a';
	alphabet_uppercase = 'B';
	number_one = 97;
	number_two = 65;
	pi = 3.14;
	fraction = 3.33;

	printf("alphabet_lowercase : %c\n", alphabet_lowercase);
	printf("alphabet_uppercase : %c\n", alphabet_uppercase);
	printf("number_one : %d\n", number_one);
	printf("number_two : %d\n", number_two);
	printf("pi : %f\n", pi);
	printf("fraction : %f\n", fraction);

	return 0;


}
